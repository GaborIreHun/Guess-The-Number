

class EnhancedGuessTheNumber
{
    public int guess;
    public string choice;

    public void SetGuess(int guessTheNumberGuess)
    {
        guess = guessTheNumberGuess;
    }

    public int GetGuess()
    {
        return guess;
    }

    public void SetChoice(string guessTheNumberChoice)
    {
        choice = guessTheNumberChoice;
    }

    public string GetChoice()
    {
        return choice;
    }
}

